void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	frameCounter++;

	if(frameCounter == 10) //After n frames have been captured, allow the below code to execute
	{
		frameCounter = 0; //Reset the counter, so n frames will be neglected again

		CDC_Transmit_FS((uint8_t*)CCDDataBuffer, 2*CCDSize); //Transfer data as binary, 2 x 8 bits
	}
	
	/*
	Little explanations:
	If your capturing code on the PC is too slow, the microcontroller might capture and send the next frame (line) before your code is able to deal with the first line. 
	Therefore, I made a simple trick that makes the code to only send every n-th line. This allows the PC to finish processing the actual line before receiving the next.
	*/
}

void checkUSBCommunication()
{
	if(commandReceived == 1)
	{
		if(buffer[0] == 's') //sampling start
		{
		  HAL_ADC_Start_DMA(&hadc1, (uint32_t*) CCDDataBuffer, CCDSize);

		  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4); //ADC
		  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1); //fM
		  HAL_TIM_PWM_Start(&htim5, TIM_CHANNEL_4); //SH
		  __HAL_TIM_SET_COUNTER(&htim2, 66);

		  //ICG is the TRIGGER timer - it is started last
		  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1); //ICG
		}
		if(buffer[0] == 'd') //sampling stop
		{
		      HAL_ADC_Stop_DMA(&hadc1);

			  HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_4); //ADC
			  HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_1); //fM
			  HAL_TIM_PWM_Stop(&htim5, TIM_CHANNEL_4); //SH
			  HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1); //ICG

			  //Resetting all counters
			  __HAL_TIM_SET_COUNTER(&htim2, 0);
			  __HAL_TIM_SET_COUNTER(&htim3, 0);
			  __HAL_TIM_SET_COUNTER(&htim4, 0);
			  __HAL_TIM_SET_COUNTER(&htim5, 0);

			  //Clear flags to make the timers behave correctly upon restart
			__HAL_TIM_CLEAR_FLAG(&htim2, TIM_FLAG_UPDATE);
			__HAL_TIM_CLEAR_FLAG(&htim3, TIM_FLAG_UPDATE);
			__HAL_TIM_CLEAR_FLAG(&htim4, TIM_FLAG_UPDATE);
			__HAL_TIM_CLEAR_FLAG(&htim5, TIM_FLAG_UPDATE);
		}
		if(buffer[0] == 'i') //integration time update (SH)
		{
			char tempStr[8 + 1];  //7 digits + 1 for null terminator
			memcpy(tempStr, &buffer[1], 7);
			tempStr[7] = '\0'; //Null-terminate
			SH_value = atoi(tempStr);

			//1 digit number example: 0000001 -> value = 1
			//3 digit number example: 0000123 -> value = 123
			//5 digit number example: 0012934 -> value = 12934
			//7 digit number example: 1243765 remains the same

			//Update the timer value
			__HAL_TIM_SET_COUNTER(&htim5,0); //Set the counter to zero first
			__HAL_TIM_SET_AUTORELOAD(&htim5, SH_value-1); //Update the ARR register
		}
		else
		{
			//skip
		}
		commandReceived = 0; //reset the flag so the code is ready to accept a new command.
	}
	
	//Note: Relevant code can be found in the usbd_cdc_if.c file in the CDC_Receive_FS() function!
}

